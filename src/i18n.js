  let currentLang = DEFAULT_LANG;

  function detectLanguage() {
    const browserLang = (navigator.language || navigator.languages?.[0] || 'en').split('-')[0].toLowerCase();
    return SUPPORTED_LANGS.includes(browserLang) ? browserLang : DEFAULT_LANG;
  }

  async function init() {
    return new Promise((resolve) => {
      chrome.storage.local.get(STORAGE_KEY, (data) => {
        if (data[STORAGE_KEY] && SUPPORTED_LANGS.includes(data[STORAGE_KEY])) {
          currentLang = data[STORAGE_KEY];
        } else {
          currentLang = detectLanguage();
          chrome.storage.local.set({ [STORAGE_KEY]: currentLang });
        }
        resolve(currentLang);
      });
    });
  }

  function setLanguage(lang) {
    if (SUPPORTED_LANGS.includes(lang)) {
      currentLang = lang;
      chrome.storage.local.set({ [STORAGE_KEY]: lang });
    }
  }

  function t(key, params) {
    let text = translations[currentLang]?.[key] || translations[DEFAULT_LANG]?.[key] || key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, v);
      }
    }
    return text;
  }

  function getLang() { return currentLang; }
  function getSupportedLangs() { return SUPPORTED_LANGS; }
  function getLangMeta() { return LANG_META; }

  return { init, setLanguage, t, getLang, getSupportedLangs, getLangMeta, detectLanguage };
